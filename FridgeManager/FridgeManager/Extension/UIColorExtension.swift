import UIKit

extension UIColor {
    class var greyishBrown: UIColor {
        return UIColor(red: 63/255, green: 58/255, blue: 58/255, alpha: 1)
    }
    class var lightGrey: UIColor {
        return UIColor(red: 216/255, green: 216/255, blue: 216/255, alpha: 1)
    }
    class var chloeYellow: UIColor {
        return UIColor(red: 254/255, green: 198/255, blue: 62/255, alpha: 1)
    }
    class var chloeBlue: UIColor {
        return UIColor(red: 63/255, green: 61/255, blue: 86/255, alpha: 1)
    }
}
